package bean;

public class Apply {
    private String 申请人姓名;
    private String 申请用品;
    private int 申请个数;
    private String 申请原因;
    private String 申请时间;
    private String 批准与否;
    private String 管理员原因或意见;


    public String get申请人姓名() {
        return 申请人姓名;
    }

    public void set申请人姓名(String 申请人姓名) {
        this.申请人姓名 = 申请人姓名;
    }

    public String get申请用品() {
        return 申请用品;
    }

    public void set申请用品(String 申请用品) {
        this.申请用品 = 申请用品;
    }

    public int get申请个数() {
        return 申请个数;
    }

    public void set申请个数(int 申请个数) {
        this.申请个数 = 申请个数;
    }

    public String get申请原因() {
        return 申请原因;
    }

    public void set申请原因(String 申请原因) {
        this.申请原因 = 申请原因;
    }

    public String get申请时间() {
        return 申请时间;
    }

    public void set申请时间(String 申请时间) {
        this.申请时间 = 申请时间;
    }

    public String get批准与否() {
        return 批准与否;
    }

    public void set批准与否(String 批准与否) {
        this.批准与否 = 批准与否;
    }

    public String get管理员原因或意见() {
        return 管理员原因或意见;
    }

    public void set管理员原因或意见(String 管理员原因或意见) {
        this.管理员原因或意见 = 管理员原因或意见;
    }
}
